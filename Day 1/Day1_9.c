#include <stdio.h>

int main(){
    // Create variables
    int myNum = 15;
    // Integers (whole numbers)
    float myFloatNum = 5.99;
    // Floating point numbers (numbers with decimal points)
    char myLetter = 'F';
    // Characters

    //Print variables
    printf("%d\n",myNum);
    printf("%f\n", myFloatNum);
    printf("%c\n", myLetter);

    return 0;
}